let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y); //3.3333333333333335
console.log(x % y);
console.log(x ** y);

//Increement Operator
console.log(++x);
console.log(x);
console.log(x++);
console.log(x);

//Decreement Operator
console.log(--x);
console.log(x--);
console.log(x);
